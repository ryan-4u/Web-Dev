//arithmetic operators
let a = 5 ;
let b = 3 ;
console.log("Arithmetic operators :");
console.log(a+b);
console.log(a-b);
console.log(a*b);
console.log(a/b);
console.log(a%b);
console.log(a**b);
//unary operators
console.log("Unary operators");
console.log(a++); // post :first use then operation
console.log(b--); // post :first use then operation
a = 2 ;
b = 2 ; // assignment operator
console.log(--a); // pre :first operation then use
console.log(++b); // pre :first operation then use

console.log("Assignment operators");
